# Global ROC curve coordinates (continuous-predictor discrimination).
# Computed only for the four base methods: the _null and _nom variants share the same continuous score columns
res_roc_original <- list()

for (red_pct in reduction_levels) {
  
  message(sprintf("\nReduction %d%%", red_pct))
  datos <- datos_por_reduccion[[as.character(red_pct)]]

  for (met in metodos) {

    message(sprintf("  %s", met$nombre))

    # Global metrics: restrict negatives to high-reporting stages to match the positive evaluation set
    etapas_alto_reporte <- stage_class[class == 1, unique(nichd)]
    neg_global <- datos$neg_high[nichd %in% etapas_alto_reporte]

    dt_global <- rbind(datos$pos_high, neg_global, fill = TRUE)
    dt_global <- detect_signal(dt_global, met$nombre, met$tipo, use_null = met$null)
    
    # Aggregate at triplet level to avoid double-counting triplets that appear in multiple stages
    metrics_global <- calculate_metrics(dt_global, n_boot, aggregate_triplet = TRUE,  # TRUE here to avoid double-counting across stages
      score_type = met$score_type, score_type_auc = met$score_type_auc)
      metrics_global[, `:=`( method = met$nombre, reduction_pct = red_pct, dataset = "original")]
      res_global_original[[length(res_global_original) + 1]] <- metrics_global

    # ROC curves for base methods only; _null/_nom share the same score column and would produce identical curves
    if (!grepl("_null|_nom", met$nombre)) {
      for (pred in c("point", "lower90")) {
        score_col <- if (pred == "point") met$score_type_auc else met$score_type
        roc_dt <- compute_roc_curve(dt_global, score_col, aggregate_triplet = TRUE)
        if (!is.null(roc_dt)) {
          roc_dt[, `:=`(method = met$nombre, reduction_pct = red_pct,
                        predictor = pred, dataset = "original")]
          res_roc_original[[length(res_roc_original) + 1]] <- roc_dt
        }
      }
    }

    # Metrics by dynamic pattern; uniform excluded (no stage subset applies)
    dinamicas <- setdiff(unique(datos$pos_high$dynamic), "uniform")

    for (dyn in dinamicas) {
      # High-reporting stages for this dynamic define the evaluation window
      etapas_altas <- stage_class[dynamic == dyn & class == 1, nichd]
      pos_dyn <- datos$pos_high[nichd %in% etapas_altas & dynamic == dyn]
      # Negatives restricted to the same high-reporting stages for comparability
      neg_dyn <- datos$neg_high[nichd %in% etapas_altas]
  
      dt_dyn <- rbind(pos_dyn, neg_dyn, fill = TRUE)
      dt_dyn <- detect_signal(dt_dyn, met$nombre, met$tipo, use_null = met$null)
      
      metrics_dyn <- calculate_metrics(dt_dyn, n_boot, aggregate_triplet = TRUE, 
        score_type = met$score_type, score_type_auc = met$score_type_auc) 
      metrics_dyn[, `:=`(
        method = met$nombre,
        reduction_pct = red_pct,
        dynamic = dyn,
        dataset = "original"
      )]
      res_dinamica_original[[length(res_dinamica_original) + 1]] <- metrics_dyn
    }
    
    # Per-stage metrics: no triplet aggregation; each row corresponds to one stage observation
    for (s in 1:7) {
      nichd_label <- niveles_nichd[s]
      # class == 1 ensures positives are from high-reporting stages for this method
      pos_stage <- datos$pos_high[stage_num == s & class == 1]
      neg_stage <- datos$neg_high[stage_num == s]
      dt_stage <- rbind(pos_stage, neg_stage, fill = TRUE)
      dt_stage <- detect_signal(dt_stage, met$nombre, met$tipo, use_null = met$null)
      metrics_stage <- calculate_metrics(dt_stage, n_boot, aggregate_triplet = FALSE, 
        score_type = met$score_type, score_type_auc = met$score_type_auc)
        metrics_stage[, `:=`(method = met$nombre, reduction_pct = red_pct, stage_num = s, nichd = nichd_label, dataset = "original")]
        res_etapa_original[[length(res_etapa_original) + 1]] <- metrics_stage
    }
  }
}

