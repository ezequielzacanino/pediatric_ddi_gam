# Global unreduced metrics for every method under each available null percentile

# Builds the merged per-stage null thresholds for an arbitrary percentile 
# mirrors the canonical construction above, but parameterized by the threshold column
build_null_thresholds <- function(pct) {
  tc <- paste0("threshold_", pct)
  nt_ior  <- fread(paste0("./results/", suffix, "/null_distribution_results/null_thresholds.csv"))[, .(stage, threshold_ior = get(tc))]
  nt_ac <- fread(paste0("./results/", suffix, "/null_distribution_results/null_thresholds_ac.csv"))[, .(stage, threshold_ac = get(tc))]
  nt_cls_ior  <- fread(ruta_cls_ior)[, .(stage, threshold_classic_ior = get(tc))]
  nt_cls_ac <- fread(ruta_cls_ac)[, .(stage, threshold_classic_ac = get(tc))]
  Reduce(function(a, b) merge(a, b, by = "stage"),
         list(nt_ior, nt_ac, nt_cls_ior, nt_cls_ac))
}

